function agent = createPGAgent(obsInfo, actInfo)
% Input path
inPath = [
    sequenceInputLayer(prod(obsInfo.Dimension), Name="obsInLyr")
    lstmLayer(128, Name="commonLSTM")
    ];

% Mean value path
meanPath = [
    reluLayer(Name="reluLayerMean");
    fullyConnectedLayer(prod(actInfo.Dimension))
    sigmoidLayer(Name="meanOutLyr")
    ];

% Standard deviation path
sdevPath = [
    reluLayer(Name="reluLayerStdv")
    fullyConnectedLayer(prod(actInfo.Dimension))
    softplusLayer(Name="stdOutLyr")
    ];

% Build network
actorNet = dlnetwork;
actorNet = addLayers(actorNet, inPath);
actorNet = addLayers(actorNet, meanPath);
actorNet = addLayers(actorNet, sdevPath);
actorNet = connectLayers(actorNet, "commonLSTM", "reluLayerMean/in");
actorNet = connectLayers(actorNet, "commonLSTM", "reluLayerStdv/in");
actorNet = initialize(actorNet);

actor = rlContinuousGaussianActor(actorNet, obsInfo, actInfo, ...
    ActionMeanOutputNames="meanOutLyr",...
    ActionStandardDeviationOutputNames="stdOutLyr",...
    ObservationInputNames="obsInLyr");

% Critic network
criticNet = [
    sequenceInputLayer(prod(obsInfo.Dimension))
    lstmLayer(128)
    reluLayer
    fullyConnectedLayer(1)
    ];

criticNet = dlnetwork(criticNet);
critic = rlValueFunction(criticNet, obsInfo);
agent = rlPGAgent(actor, critic);


